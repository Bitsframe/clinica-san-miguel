import "regenerator-runtime/runtime";
import type { Metadata } from "next";
import { Inter } from "next/font/google";
import { Poppins } from "next/font/google";
import "./globals.css";
import { Footer, Navbar } from "@/components";

// Provider
import { Providers } from "../providers";
import AiBotMain from "@/components/AiChatBot/AiBotMain";

import i18nConfig from "@/i18nConfig";
import { dir } from "i18next";
import { LanguageSelectionDesc } from "@/components/Bar";

const inter = Inter({
  subsets: ["latin"],
  variable: "--font-inter",
  display: "swap",
});

const poppins = Poppins({
  subsets: ["latin"],
  variable: "--font-poppins",
  display: "swap",
  weight: "400",
});

export const metadata: Metadata = {
  title: "Clinca San Miguel",
  description: "Generated by create next app",
};

export function generateStaticParams() {
  return i18nConfig.locales.map((locale: string) => ({ locale }));
}

export default function RootLayout({
  children,
  params: { locale },
}: {
  children: React.ReactNode;
  params: { locale: string };
}) {
  return (
    <html
      lang={locale}
      dir={dir(locale)}
      className={`${inter.variable} ${poppins.variable}`}
    >
      <body className="font-inter bg-[#F8F5F0] relative overflow-x-hidden w-[100vw]">
        <Providers>
          <LanguageSelectionDesc />
          <Navbar />
          {children}

          <Footer />
          <div
            className="sticky z-[999999] bottom-10 right-10"
            style={{ zIndex: 999999 }}
          >
            <AiBotMain />
          </div>
        </Providers>
      </body>
    </html>
  );
}
